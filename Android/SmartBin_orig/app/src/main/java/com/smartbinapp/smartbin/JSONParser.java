                Log.d("in jhttpreq url ","hehehe "+url.toString());
                Log.d("in jsonreq httpget ","hehehe "+httpGet.toString());
                HttpResponse httpResponse = httpClient.execute(httpGet);
                Log.d("in jsonpgetresponse ","hehehe "+ httpResponse);
